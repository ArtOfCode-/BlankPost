<h3>There's no user account associated with this ID.</h3>
<h4>We couldn't find details of any user account with the ID number your browser provided.</h4>
<p>In simple terms, this user account doesn't exist!</p>
<p><strong>You what?</strong></p>
<p>This error occurred while trying to look up your user account to insert details into your account page.
Essentially, there are no details associated with the ID that was stored when you logged in.</p>
<p><strong>Why?</strong></p>
<p>The only <i>known</i> cause for this error is when you're logged in and your account gets deleted. When
this happens, your details are removed from our database, but you remain logged in because the necessary information
is still stored in your browser.</p>
<p><strong>What now?</strong></p>
<p>If you deleted your own account, that'll be why you're seeing this error. Log out now. You won't be able to log
back into this account.</p>
<p>If you <em>didn't</em> delete your own account, then there are two possible reasons. First: that someone with
administrator access has deleted your account. You can <a href="/contact">contact me</a> if you want to find out more.
Second: that something has gone <em>horribly</em> wrong here. Again, your best option is to <a href="/contact">
contact me</a> to tell me about this.
